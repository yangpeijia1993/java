package com.perlina.java.foundational.control.forstatement;

/**
 * 打印100-1
 *
 * @author: Perlina
 * @version:2021/8/2111:22 PM
 */
public class For100To1 {
    public static void main(String[] args) {
        //打印100-1
        for (int i=100;i>=1;i--){
            System.out.print(i+" ");
        }
    }
}
