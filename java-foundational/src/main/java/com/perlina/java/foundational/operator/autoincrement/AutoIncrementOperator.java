package com.perlina.java.foundational.operator.autoincrement;

/**
 * 自增运算
 *
 * @author: Perlina
 * @version:2021/8/214:08 PM
 */
public class AutoIncrementOperator {
    public static void main(String[] args) {
        //后++
        int num = 10;
        num++;
        System.out.println(num);

        //前++
        int num2 = 10;
        ++num2;
        System.out.println(num2);
    }
}
