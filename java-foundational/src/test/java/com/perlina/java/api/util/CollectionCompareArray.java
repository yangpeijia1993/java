package com.perlina.java.api.util;

/**
 * 数组集合比较
 *
 * @author: Perlina
 * @version:2021/8/284:20 下午
 */
public class CollectionCompareArray {
    public static void main(String[] args) {
        /**
         * 数组
         * 一旦初始化后长度不能改变
         * 数组存储的元素必须数据类型一致，要么是基本数据类型，要么是引用数据类型
         * 数组的缺点：数组的操作很麻烦，没有提供管理数据（数据的增删改查）的方法
         *
         * 集合
         * 集合的长度可以动态变化
         * 集合存储的数据类型智能是引用数据类型，如果存储的是基本数据类型，会自动装箱成对应的包装类
         * 集合可以存储任意的数据类型
         * 集合的优点：集合操作数据非常方便
         */
    }
}
